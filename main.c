int32_t nzer (int32_t* array_ptr, size_t array_size)
{
    // кол-во нулевых элементов
	int32_t nz = 0;
	int32_t i = 0;
	
	while (i < array_size)
	{
        if (*(array_ptr+i)==0)
            nz++;
		i++;
	}
    
	return nz;
}

int32_t sneg (int32_t* array_ptr, size_t array_size)
{
    // сумма отрицательных элементов
	int32_t sn = 0;
	int32_t i = 0;
	
	while (i < array_size)
    {
        if (*(array_ptr+i)<0)
            sn = sn+*(array_ptr+i);
		i++;
    }
    
	return sn;
}

int32_t mpos (int32_t* array_ptr, size_t array_size)
{
    // произведение положительных элементов
	int32_t mp = 1;
	int32_t i = 0;
	
	while (i < array_size)
	{
        if (*(array_ptr+i)>0)
            mp = mp**(array_ptr+i);
		i++;
	}
    
	return mp;
}
